#variavel e entrada
num1 = int(input('Escreva um número: '))

#2variavel e operações
dobro = num1 * 2
triplo = num1 * 3
raiz = num1 ** (1/2)

#mostrar na tela
print(f'O dobro deste número é {dobro}')
print(f'O triplo deste número é {triplo}')
print('O raiz deste número é {:.2f}'.format(raiz))
